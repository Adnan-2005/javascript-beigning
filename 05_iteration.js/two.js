// WHILE LOOP

let i = 0
while (i<=10) {
    //console.log(`Value of i is : ${i}`);
    i += 2
}

let myArray = ["Flash" , "Batman" , "Superman"]
let arr = 0
while (arr<myArray.length) {
    //console.log(`Vlaue is ${myArray[arr]}`);
    arr++
}

// DO WHILE LOOP
let score = 1 
do {
    console.log(`Score is : ${score}`);
    score++
} while (score<=10);